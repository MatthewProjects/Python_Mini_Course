def main():
    # Create a variable called `num` and assign it with a numeric value of `10`.
    num = 10

    # Add `5` to `num`, and assign the result to `num`.
    num += 5
    print(num)  # Output: 15

    # Subtract `3` from `num`, and assign the result to `num`.
    num -= 3
    print(num)  # Output: 12

    # Multiply `num` by `2`, and assign the result to `num`.
    num *= 2
    print(num)  # Output: 24

    # Divide `num` by `4`, and assign the result to `num`.
    num /= 4
    print(num)  # Output: 6.0

    # `num2` is defined and has a value of 6
    num2 = 6

    # Multiply `num` by `num2`, and assign the result to `num`.
    num *= num2
    print(num)  # Output: 36.0

if __name__ == "__main__":
    main()
